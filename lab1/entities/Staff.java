package entities;

public abstract class Staff {
    
    protected int id;
    protected String name;

    public Staff() {}
    
    public Staff(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
